#include <iostream>

using namespace std;

int main(){
	
	//Esto es una variable de tipo entero
	int entero = 44;
	cout<<"Valor del entero: "<<entero;
	//Esto es un salto de línea
	cout<<"\n";
	//Esto es una variable de tipo flotante
	float flotante = 4.2233;
	cout<<"Valor del flotante: "<<flotante<<"\n";
	//Esto es una variable de tipo doble
	double doble = 3.1416;
	cout<<"Valor del doble: "<<doble<<"\n";
	//Esto es una variable de tipo char
	char letra = 'a';
	cout<<"Letra: "<<letra<<"\n";
	//Esto es una variable de tipo booleano
	// Se puede guardar como '1' o '0' (true o false) pero
	// es más recomendable guardarlas como true o false
	bool booleano = true;
	cout<<"Valor del booleano: "<<booleano;
	
	return 0;
}
